public class HelenaMaia_Exercicio04{
    public static void main(String[] args){
        //limita entre 0 e 1000 e incrementa +2
        for(int cont = 1; cont <= 1000; cont += 2){
            System.out.println(cont);//mostra os numeros
    }
}
}