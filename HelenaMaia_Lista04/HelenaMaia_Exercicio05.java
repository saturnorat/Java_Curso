public class HelenaMaia_Exercicio05 {
    public static void main(String[] args) {
            //cria n e enquanto n for maior ou igual a 0, n - 1
        for (int n = 10; n >= 0; n--) {
            System.out.println(n);
        }
        //mostra fim
        System.out.println("Fim");
    }
}
